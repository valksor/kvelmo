func TestHandler_GetTaskCosts_PathParameter(t *testing.T) {
	cfg := Config{
		Port:      0,
		Mode:      ModeProject,
		Conductor: nil,
	}

	srv, cleanup := startTestServer(t, cfg)
	defer cleanup()

	ctx := context.Background()
	client := testHTTPClient()

	// Different task IDs
	taskIDs := []string{"abc123", "task-with-dashes", "UPPER123", "123numeric"}
	for _, taskID := range taskIDs {
		t.Run(taskID, func(t *testing.T) {
			resp, err := doGet(ctx, client, srv.URL()+"/api/v1/tasks/"+taskID+"/costs")
			require.NoError(t, err)
			defer func() { _ = resp.Body.Close() }()

			// All fail on conductor check
			assert.Equal(t, http.StatusServiceUnavailable, resp.StatusCode)
		})
	}
}
