func TestCreateSkipsTransientFiles(t *testing.T) {
	srcDir := t.TempDir()

	// Create a regular file and transient files
	if err := os.WriteFile(filepath.Join(srcDir, "config.yaml"), []byte("data"), 0o644); err != nil {
		t.Fatalf("Failed to create config.yaml: %v", err)
	}
	if err := os.WriteFile(filepath.Join(srcDir, "server.sock"), []byte(""), 0o644); err != nil {
		t.Fatalf("Failed to create server.sock: %v", err)
	}
	if err := os.WriteFile(filepath.Join(srcDir, "db.lock"), []byte(""), 0o644); err != nil {
		t.Fatalf("Failed to create db.lock: %v", err)
	}

	outDir := t.TempDir()
	outPath := filepath.Join(outDir, "backup.tar.gz")

	result, err := Create(srcDir, outPath)
	if err != nil {
		t.Fatalf("Create() failed: %v", err)
	}

	// Only the config.yaml should be in the archive (not .sock or .lock)
	if result.Files != 1 {
		t.Errorf("Result.Files = %d, want 1 (transient files should be skipped)", result.Files)
	}

	// Verify archive does not contain transient files
	names := listArchiveEntries(t, outPath)
	for _, name := range names {
		if strings.HasSuffix(name, ".sock") || strings.HasSuffix(name, ".lock") {
			t.Errorf("Archive contains transient file: %s", name)
		}
	}
}

