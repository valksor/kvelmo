// verifyArchiveContents checks that the tar.gz archive contains the expected files with correct content.
func verifyArchiveContents(t *testing.T, archivePath string, expected map[string]string) {
	t.Helper()

	f, err := os.Open(archivePath)
	if err != nil {
		t.Fatalf("Failed to open archive: %v", err)
	}
	defer func() { _ = f.Close() }()

	gz, err := gzip.NewReader(f)
	if err != nil {
		t.Fatalf("Failed to create gzip reader: %v", err)
	}
	defer func() { _ = gz.Close() }()

	tr := tar.NewReader(gz)
	found := make(map[string]string)

	for {
		header, err := tr.Next()
		if errors.Is(err, io.EOF) {
			break
		}
		if err != nil {
			t.Fatalf("Failed to read tar entry: %v", err)
		}

		if header.Typeflag == tar.TypeDir {
			continue
		}

		data, err := io.ReadAll(tr)
		if err != nil {
			t.Fatalf("Failed to read tar entry content: %v", err)
		}
		found[header.Name] = string(data)
	}

	for name, wantContent := range expected {
		gotContent, ok := found[name]
		if !ok {
			t.Errorf("Expected file %q not found in archive", name)

			continue
		}
		if gotContent != wantContent {
			t.Errorf("File %q content = %q, want %q", name, gotContent, wantContent)
		}
	}
}

// listArchiveEntries returns the names of all entries in a tar.gz archive.
func listArchiveEntries(t *testing.T, archivePath string) []string {
	t.Helper()

	f, err := os.Open(archivePath)
	if err != nil {
		t.Fatalf("Failed to open archive: %v", err)
	}
	defer func() { _ = f.Close() }()

	gz, err := gzip.NewReader(f)
	if err != nil {
		t.Fatalf("Failed to create gzip reader: %v", err)
	}
	defer func() { _ = gz.Close() }()

	tr := tar.NewReader(gz)
	var names []string

	for {
		header, err := tr.Next()
		if err == io.EOF {
			break
		}
		if err != nil {
			t.Fatalf("Failed to read tar entry: %v", err)
		}
		names = append(names, header.Name)
	}

	return names
}
