func TestIsTransientFile(t *testing.T) {
	tests := []struct {
		name string
		want bool
	}{
		{"global.sock", true},
		{"worktree.sock", true},
		{"db.lock", true},
		{"main.go", false},
		{"config.yaml", false},
		{"backup.tar.gz", false},
		{"readme.md", false},
		{".sock", true},
		{".lock", true},
		{"path/to/file.sock", true},
		{"path/to/file.lock", true},
		{"path/to/file.go", false},
	}

	for _, tt := range tests {
		got := IsTransientFile(tt.name)
		if got != tt.want {
			t.Errorf("IsTransientFile(%q) = %v, want %v", tt.name, got, tt.want)
		}
	}
}

