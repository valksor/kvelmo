func TestCreateBasicBackup(t *testing.T) {
	// Create a temp directory with some files to back up
	srcDir := t.TempDir()

	files := map[string]string{
		"config.yaml": "key: value",
		"data.json":   `{"hello": "world"}`,
	}
	for name, content := range files {
		if err := os.WriteFile(filepath.Join(srcDir, name), []byte(content), 0o644); err != nil {
			t.Fatalf("Failed to create test file %s: %v", name, err)
		}
	}

	// Create backup with explicit output path
	outDir := t.TempDir()
	outPath := filepath.Join(outDir, "test-backup.tar.gz")

	result, err := Create(srcDir, outPath)
	if err != nil {
		t.Fatalf("Create() failed: %v", err)
	}

	if result.Files != len(files) {
		t.Errorf("Result.Files = %d, want %d", result.Files, len(files))
	}
	if result.Size <= 0 {
		t.Errorf("Result.Size = %d, want > 0", result.Size)
	}
	if result.Path != outPath {
		t.Errorf("Result.Path = %q, want %q", result.Path, outPath)
	}

	// Verify the archive contents
	verifyArchiveContents(t, outPath, files)
}

func TestCreateAutoGeneratedFilename(t *testing.T) {
	srcDir := t.TempDir()
	if err := os.WriteFile(filepath.Join(srcDir, "file.txt"), []byte("hello"), 0o644); err != nil {
		t.Fatalf("Failed to create test file: %v", err)
	}

	// Use a temp dir as working directory so the auto-generated file lands there
	outDir := t.TempDir()
	t.Chdir(outDir)

	result, err := Create(srcDir, "")
	if err != nil {
		t.Fatalf("Create() with empty outputPath failed: %v", err)
	}

	if !strings.HasPrefix(filepath.Base(result.Path), "kvelmo-backup-") {
		t.Errorf("Auto-generated filename %q does not start with kvelmo-backup-", filepath.Base(result.Path))
	}
	if !strings.HasSuffix(result.Path, ".tar.gz") {
		t.Errorf("Auto-generated filename %q does not end with .tar.gz", result.Path)
	}

	t.Cleanup(func() { _ = os.Remove(result.Path) })
}

