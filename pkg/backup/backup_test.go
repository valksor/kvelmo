func TestListBackups(t *testing.T) {
	dir := t.TempDir()

	// Create backup files with correct naming pattern
	backupFiles := []string{
		"kvelmo-backup-20260101-120000.tar.gz",
		"kvelmo-backup-20260102-120000.tar.gz",
	}
	for _, name := range backupFiles {
		if err := os.WriteFile(filepath.Join(dir, name), []byte("fake"), 0o644); err != nil {
			t.Fatalf("Failed to create %s: %v", name, err)
		}
	}

	// Create files that should NOT match
	nonBackupFiles := []string{
		"other-file.tar.gz",
		"kvelmo-backup-incomplete",
		"kvelmo-backup-.txt",
		"random.txt",
	}
	for _, name := range nonBackupFiles {
		if err := os.WriteFile(filepath.Join(dir, name), []byte("ignore"), 0o644); err != nil {
			t.Fatalf("Failed to create %s: %v", name, err)
		}
	}

	// Create a subdirectory with backup-like name (should be skipped)
	if err := os.MkdirAll(filepath.Join(dir, "kvelmo-backup-dir.tar.gz"), 0o755); err != nil {
		t.Fatalf("Failed to create dir: %v", err)
	}

	backups, err := List(dir)
	if err != nil {
		t.Fatalf("List() failed: %v", err)
	}

	if len(backups) != len(backupFiles) {
		t.Fatalf("List() returned %d backups, want %d", len(backups), len(backupFiles))
	}

	for _, b := range backups {
		if !strings.HasPrefix(b.Name, "kvelmo-backup-") {
			t.Errorf("Backup name %q does not have expected prefix", b.Name)
		}
		if b.Size <= 0 {
			t.Errorf("Backup %q has size %d, want > 0", b.Name, b.Size)
		}
		if b.CreatedAt == "" {
			t.Errorf("Backup %q has empty CreatedAt", b.Name)
		}
		if b.Path == "" {
			t.Errorf("Backup %q has empty Path", b.Name)
		}
	}
}

